#!/bin/bash -x
read var1
if [ $var1 -eq 1 ]
then
         echo "one"
elif [ $var1 -eq 2 ]
then
         echo "two"

elif [ $var1 -eq 3 ]
then
         echo "three"


elif [ $var1 -eq 4 ]
then
         echo "four"


elif [ $var1 -eq 5 ]
then
         echo "five"


elif [ $var1 -eq 6 ]
then
         echo "six"

elif [ $var1 -eq 7 ]
then
         echo "seven"


elif [ $var1 -eq 8 ]
then
         echo "eight"

else
         echo "nine"
fi
